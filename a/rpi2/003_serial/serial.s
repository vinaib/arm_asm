@@ FILE: serial.s

@@ Serial Debug pins
@@ GPIO14: TXD0	ALT0
@@ GPIO15: RXD0	ALT0
@@ GPIO32: TXD0 ALT3
@@ GPIO33: RXD0 ALT3
@@ GPIO36: TXD0 ALT2(Pull High)
@@ GPIO37: RXD0 ALT2
@@
@@ GPIO16,GPIO30: CTS0 ALT3
@@ GPIO17,GPIO31: RTS0 ALT3
@@ GPIO38: RTS0, ALT2
@@ GPIO39: CTS0, ALT2

@@ Correction:
@@ In BCM28355 ARM Peripherals.docx it is printed as 0x7E20100.
@@ The correct one is PL011 USRT base address 0x7E201000.
@@ ARM space: 0x3F201000

	.equ	UART_BASE		0x3F201000

	.equ	UART_DR			0x3F201000		@@ DATA REG
	.equ	UART_RSRECR		0x3F201004
	.equ	UART_FR			0x3F201018		@@ FLAG REG
	.equ	UART_ILPR		0x3F201020		@@ NOT IN USE
	.equ	UART_IBRD		0x3F201024		@@ INTEGER BAUD RATE DIVISOR
	.equ	UART_FBRD		0X3F201028		@@ FRACTIONAL BAUD RATE DIVISOR
	.equ	UART_LCRH		0X3F20102C		@@ LINE CONTROL REG
	.equ	UART_CR			0X3F201030		@@ CONTROL REG
	.equ	UART_IFLS		0X3F201034		@@ INTR FIFO LEVEL SELECT REG
	.equ	UART_IMSC		0X3F201038		@@ INTR MASK SET CLR REG
	.equ	UART_RIS		0X3F20103C		@@ RAW INTR STATUS REG
	.equ	UART_MIS		0X3F201040		@@ MASKED INTR STATUS REG
	.equ	UART_ICR		0X3F201044		@@ INTR CLR REG
	.equ	UART_DMACR		0X3F201048		@@ DMA CONTROL REG
	.equ	UART_ITCR		0X3F201080		@@ TEST CONTROL REG
	.equ	UART_ITIP		0X3F201084		@@ INTEGRATION TEST INPUT REG
	.equ	UART_ITOP		0X3F201088		@@ INTEGRATION TEST OUTPUT REG
	.equ	UART_TDR		0X3F20108C		@@ TEST DATA REG

